package com.fssa.politifact.enums;

/*
 * This enum represents various political positions:
 * - CHIEF_MINISTER: The head of the state government.
 * - COUNCIL_MINISTER: A minister in the state government.
 * - MEMBER_OF_LEGISLATIVE_ASSEMBLY: A member of the state legislative assembly.
 * - MEMBER_OF_PARLIAMENT: A member of the national parliament.
 */

public enum Position {
    CHIEF_MINISTER,
    COUNCIL_MINISTER,
    MEMBER_OF_LEGISLATIVE_ASSEMBLY,
    MEMBER_OF_PARLIAMENT;

}

